package co.in.pp;

public class WifeDTO 
{

	
	private Integer wid, wage, wmobile;
	
	private String wname, wgender, woccupation;
	
	private ChildDTO child;
	
	

	public Integer getWid() {
		return wid;
	}

	public void setWid(Integer wid) {
		this.wid = wid;
	}

	public Integer getWage() {
		return wage;
	}

	public void setWage(Integer wage) {
		this.wage = wage;
	}

	public Integer getWmobile() {
		return wmobile;
	}

	public void setWmobile(Integer wmobile) {
		this.wmobile = wmobile;
	}

	public String getWname() {
		return wname;
	}

	public void setWname(String wname) {
		this.wname = wname;
	}

	public String getWgender() {
		return wgender;
	}

	public void setWgender(String wgender) {
		this.wgender = wgender;
	}

	public String getWoccupation() {
		return woccupation;
	}

	public void setWoccupation(String woccupation) {
		this.woccupation = woccupation;
	}

	public ChildDTO getChild() {
		return child;
	}

	public void setChild(ChildDTO child) {
		this.child = child;
	}
	
		
}
