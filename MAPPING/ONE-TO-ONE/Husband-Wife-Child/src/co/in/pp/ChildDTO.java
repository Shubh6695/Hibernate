package co.in.pp;

public class ChildDTO 
{

	private Integer cid, cage, cmobile;
	
	private String cname, cgender, ceducation;

	
	
	public Integer getCid() {
		return cid;
	}

	public void setCid(Integer cid) {
		this.cid = cid;
	}

	public Integer getCage() {
		return cage;
	}

	public void setCage(Integer cage) {
		this.cage = cage;
	}

	public Integer getCmobile() {
		return cmobile;
	}

	public void setCmobile(Integer cmobile) {
		this.cmobile = cmobile;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public String getCgender() {
		return cgender;
	}

	public void setCgender(String cgender) {
		this.cgender = cgender;
	}

	public String getCeducation() {
		return ceducation;
	}

	public void setCeducation(String ceducation) {
		this.ceducation = ceducation;
	}
	
	
}
